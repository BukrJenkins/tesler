package io.tesler.core.diag.jdbc;

import com.p6spy.engine.spy.P6DataSource;
import javax.sql.DataSource;
import lombok.RequiredArgsConstructor;
import org.springframework.beans.BeansException;
import org.springframework.beans.factory.config.BeanPostProcessor;
import org.springframework.stereotype.Component;


@RequiredArgsConstructor
@Component
public class DiagDataSourcePostProcessor implements BeanPostProcessor {

	final DiagJdbcEventListenerFactory eventListenerFactory;

	@Override
	public Object postProcessBeforeInitialization(Object bean, String beanName) throws BeansException {
		return bean;
	}

	@Override
	public Object postProcessAfterInitialization(Object bean, String beanName) throws BeansException {
		if (bean instanceof DataSource) {
			P6DataSource dataSource = new P6DataSource((DataSource) bean);
			dataSource.setJdbcEventListenerFactory(eventListenerFactory);
			return dataSource;
		}
		return bean;
	}

}
